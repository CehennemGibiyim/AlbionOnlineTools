let currentLang = localStorage.getItem('albion-lang') || 'tr';

function t(key) {
    return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key])
        || TRANSLATIONS['en'][key]
        || key;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('albion-lang', lang);
    applyTranslations();
    // Update active state in lang grid
    document.querySelectorAll('.lang-btn').forEach(b => {
        b.classList.toggle('active', b.dataset.lang === lang);
    });
}

window.I18n = { t, setLanguage, applyTranslations, currentLang: () => currentLang, TRANSLATIONS };
